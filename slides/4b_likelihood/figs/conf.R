dir <- "f:/verk/assmt-namskeid/1/biolike/figs"

width <- 5
height <- 2.5

conf.1 <- paste(dir, "conf-1.eps", sep="/")
conf.2 <- paste(dir, "conf-2.eps", sep="/")
conf.3 <- paste(dir, "conf-3.eps", sep="/")
conf.4 <- paste(dir, "conf-4.eps", sep="/")
conf.5 <- paste(dir, "conf-5.eps", sep="/")

theta <- seq(120, 180, 1)
logL <- dnorm(theta, 150, 10, log=TRUE)
yopt <- -dnorm(150, 150, 10, log=TRUE)
y95 <- yopt + 0.5*qchisq(0.95,1)
xlo <- 130.4
xhi <- 169.6
w <- 0.15

eps(conf.1, width=width, height=height, pointsize=8)
par(plt=c(0.25,0.85, 0.25,0.85))
plot(theta, -logL, type="l", ann=FALSE, xaxt="n", las=1)
axis(1)
box()
title(xlab=expression(theta))
title(ylab="-log L")
dev.off()
eps2pdf(conf.1)

eps(conf.2, width=width, height=height, pointsize=8)
par(plt=c(0.25,0.85, 0.25,0.85))
plot(theta, -logL, type="l", ann=FALSE, xaxt="n", las=1)
axis(1)
box()
title(xlab=expression(theta))
title(ylab="-log L")
arrows(150, yopt, 150, y95, length=0.08, lwd=0.5)
text(150, mean(c(yopt,y95)), adj=0, pos=2, offset=0.3, cex=0.9, expression(Delta~{}=={}))
text(150, mean(c(yopt,y95)), adj=0, pos=4, offset=0.3, cex=0.9, expression(1.92))
dev.off()
eps2pdf(conf.2)

eps(conf.3, width=width, height=height, pointsize=8)
par(plt=c(0.25,0.85, 0.25,0.85))
plot(theta, -logL, type="l", ann=FALSE, xaxt="n", las=1)
axis(1)
box()
title(xlab=expression(theta))
title(ylab="-log L")
arrows(150, yopt, 150, y95, length=0.08, lwd=0.5)
text(150, mean(c(yopt,y95)), adj=0, pos=2, offset=0.3, cex=0.9, expression(Delta~{}=={}))
text(150, mean(c(yopt,y95)), adj=0, pos=4, offset=0.3, cex=0.9, expression(1.92))
lines(c(xlo,xhi), c(y95,y95), lty=3, length=0.08)
dev.off()
eps2pdf(conf.3)

eps(conf.4, width=width, height=height, pointsize=8)
par(plt=c(0.25,0.85, 0.25,0.85))
plot(theta, -logL, type="l", ann=FALSE, xaxt="n", las=1)
axis(1)
box()
title(xlab=expression(theta))
title(ylab="-log L")
arrows(150, yopt, 150, y95, length=0.08, lwd=0.5)
text(150, mean(c(yopt,y95)), adj=0, pos=2, offset=0.3, cex=0.9, expression(Delta~{}=={}))
text(150, mean(c(yopt,y95)), adj=0, pos=4, offset=0.3, cex=0.9, expression(1.92))
lines(c(xlo,xlo,xhi,xhi), c(par("usr")[3],y95,y95,par("usr")[3]), lty=3)
dev.off()
eps2pdf(conf.4)

eps(conf.5, width=width, height=height, pointsize=8)
par(plt=c(0.25,0.85, 0.25,0.85))
plot(theta, -logL, type="l", ann=FALSE, xaxt="n", las=1)
axis(1)
box()
title(xlab=expression(theta))
title(ylab="-log L")
arrows(150, yopt, 150, y95, length=0.08, lwd=0.5)
text(150, mean(c(yopt,y95)), adj=0, pos=2, offset=0.3, cex=0.9, expression(Delta~{}=={}))
text(150, mean(c(yopt,y95)), adj=0, pos=4, offset=0.3, cex=0.9, expression(1.92))
lines(c(xlo,xlo,xhi,xhi), c(par("usr")[3],y95,y95,par("usr")[3]), lty=3)
lines(c(xlo,xhi), c(yopt,yopt), lwd=4, col="red")
lines(c(xlo,xlo), c(yopt-w,yopt+w), lwd=4, col="red")
lines(c(xhi,xhi), c(yopt-w,yopt+w), lwd=4, col="red")
dev.off()
eps2pdf(conf.5)

unlink(c(conf.1, conf.2, conf.3, conf.4, conf.5))
